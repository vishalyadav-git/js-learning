const month = "april"
switch (month) {
    case "jan":
        console.log("january");
        break;
    case "feb":
        console.log("febuary");
        break;
    case "mar":
        console.log("march");
        break;
    case "april":
        console.log("april");
        break;
    case "may":
        console.log("may");
        break;

    default:
        console.log("default value");
        break;
}

